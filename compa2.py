#Operasi Perbandingan : 
# > (bigger than)
# < (smaller than)
# >= (equal to or bigger than)
# <= (equal to or smaller than)
# != (unequal to)
# == (equal to)

x = 21
y = 20
print( "x > y = ", x > y) # True
print( "x < y = ", x < y) # False
print( "x >= y = ", x >= y) # True
print( "x <= y = ", x <= y) # False
print( "x != y = ", x != y) # False
print( "x == y = ", x == y) # False